function loadMedicines100() {
    const slider = document.getElementById('medicinesSlider');
    if (!slider) return;

    let html = "";
    medicinesArray.forEach(item => {
        html += `
            <div class="slide">
                <img src="${item.img}" alt="${item.name}" loading="lazy">
                <div class="slide-caption">تصویر ${item.id}: ${item.name}</div>
            </div>
        `;
    });
    slider.innerHTML = html;

    // Auto Engine Scroll inside its own box
    setInterval(() => {
        if (slider.scrollLeft <= -(slider.scrollWidth - slider.clientWidth - 5)) {
            slider.scrollLeft = 0;
        } else {
            slider.scrollBy({ left: -290, behavior: 'smooth' });
        }
    }, 2800);
}

